            <p>El técnico en computación cuenta con una preparación que lo habilita para asistir eficazmente al profesional en la mayoría de las resoluciones de programas, fundamentalmente de tipo administrativo, contable y científico.</p> 
          <p>
            En la actualidad el perfil del Técnico en Computación se van actualizando del plan original en función de las innovaciones que se producen en los campos tecnológicos y ocupacionales. Las innovaciones mayores fueron realizadas en la programación de contenidos de las distintas asignaturas. Esta estrategia de innovación tuvo como efecto que cada establecimiento definió la pertinencia de ciertos contenidos sobre otros en función de cada proyecto institucional particular. La consecuencia sobre el perfil formativo y profesional es la definición de trayectorias heterogéneas de formación y diferencias de perfil formativo.

             Dentro de la heterogeneidad de situaciones abiertas en las 11 escuelas, las principales líneas de innovación, se presentan en dos niveles:
             – Incorporación de contenidos que no alteran la orientación original del perfil de la especialidad: cambiando contenidos de los programas de asignaturas y laboratorios, mantienen los propósitos formativos específicos para esta especialidad, centrados en la programación. Estas innovaciones radican en la incorporación de lenguajes de programación y constituyen el mecanismo usual de actualización en un campo fuertemente condicionado por una importante dinámica de innovación tecnológica.
             – Incorporación de contenidos que tienden a modificar la orientación original del perfil de la especialidad: Implican reorientaciones significativas del perfil de egreso. Las dos líneas más difundidas de innovación que pudieron identificarse son la incorporación de:
             <ul>
               <li>
