        <h1>Alumnos crearon un sistema para atenuar los efectos de las inundaciones</h1>
        <p style="text-align: justify;">Cualquier pronóstico meteorológico, por más preciso que sea, resulta insuficiente. La lluvia sorprende intensa en medio de la noche y en los barrios más abandonados por la desidia del Estado el agua sube de un momento a otro. Sin pedir permiso, se mete en las casas y lo arruina todo, sea poco o mucho lo que se tenga.</p>

<p style="text-align: justify;">De eso saben bastante los vecinos del populoso Barrio Mitre, que ocupa 16 manzanas en Saavedra delimitadas por las calles Melián, Correa, Posta y Arias, en terrenos que se encuentran más bajos comparados con el resto de la Ciudad de Buenos Aires. Inaugurado en 1958 para albergar a vecinos afectados por el incendio de un asentamiento, hoy la cantidad de habitantes del barrio se multiplicó: a las casas de material, se sumaron otras más precarias, pero aún no cuenta con la suficiente infraestructura como para soportar las inclemencias del tiempo. Las tuberías y cloacas existentes no dan abasto para frenar la crecida del arroyo Medrano -que pasa por debajo de las avenidas Ruiz Hidobro y García del Río-, y la construcción del imponente Shopping Dot en 2009 agravó aún más la situación. Según los vecinos, cada vez que llueve el centro comercial descarga sus desagües sobre el barrio.  </p>  
